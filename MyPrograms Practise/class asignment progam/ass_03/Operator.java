public class Operator
{
	public static void main(String args[])
		{
			int a=32;
			System.out.println(a>>2);
			System.out.println(a<<2);
			
			int b=15;
			a=40;
			System.out.println(a&b);
			System.out.println(a|b);
			System.out.println(a^b);

			int i=-1;
			System.out.println(i>>24);
			System.out.println(i>>>24);


			
		}
}